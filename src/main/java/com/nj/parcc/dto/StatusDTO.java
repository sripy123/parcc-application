package com.nj.parcc.dto;

/**
 * Status DTO class
 * @author Sriram
 *
 */
public class StatusDTO {

	private String code;
	private String msg;
	
	public StatusDTO() {
		
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	
}
